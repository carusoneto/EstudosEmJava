// e dentro do package nós temos a nossa class Main
// por que essa class é "public" aqui eu to querendo dizer que ela pode ser acesasda por outros pacotes
public class Main {
    // e dentro da class nós temos o metodo main
    public static void main(String[] args) // string[] args são parametros do metodo main
    {
        // e dentro do main nós temos o stantment;
        System.out.println("Hello world!");
    }
}

